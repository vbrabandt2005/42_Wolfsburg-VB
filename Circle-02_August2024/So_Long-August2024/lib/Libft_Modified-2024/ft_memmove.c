#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t len)
{
	unsigned char		*d;
	const unsigned char	*s;
	unsigned char		*lasts;
	unsigned char		*lastd;

	d = (unsigned char *)dest;
	s = (const unsigned char *)src;
	if (!dest && !src)
		return (NULL);
	if (d == s)
		return (dest);
	else if (d < s)
	{
		while (len--)
			*d++ = *s++;
	}
	else
	{
		lasts = (unsigned char *)s + (len - 1);
		lastd = d + (len - 1);
		while (len--)
			*lastd-- = *lasts--;
	}
	return (dest);
}

