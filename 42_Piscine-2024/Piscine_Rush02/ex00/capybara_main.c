int	main(int argc, char **argv)
{
	int		error;
	char	***dictionary;

	error = validate_arguments(argc, argv);
	if (error)
	{
		print_error(error);
		return (0);
	}
	***dictionary = allocate_dictionary();
	get_dict(MAIN_DICTIONARY, dictionary);
	if (argc == 2)
	{
		handle_read_mode(dictionary, argv[1]);
	}
	else
	{
		handle_write_mode(dictionary, argv[1], argv[2]);
	}
	free_dictionary(dictionary);
	return (0);
}

