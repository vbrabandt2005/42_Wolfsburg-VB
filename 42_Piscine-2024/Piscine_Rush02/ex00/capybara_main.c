//validate_arguments(argc, argv)
int	validate_arguments(int argc, char **argv)
{
	if (argc < 2 || argc > 3)
	{
		write (1, "Error\n", 6);
		return (ERROR_INVALID_ARGUMENTS);
	}
	if (!str_is_numeric(argv[1]))
	{
		return (ERROR_INVALID_RECORD_NUMBER);
	}
	if (argc == 3 && !entry_is_valid(argv[2]))
	{
		return (ERROR_INVALID_ENTRY_VALUE);
	}
	return (0);
}

// allocate_dictionary()
char	***allocate_dictionary(void)
{
	char	***dictionary;
	int		record;
	int		entry;

	***dictionary = (char ***)malloc(sizeof(char **) * 60);
	record = 0;
	while (record < 50)
	{
		dictionary[record] = (char **)malloc(sizeof(char *) * 2);
		entry = 0;
		while (entry < 2)
		{
			dictionary[record][entry] = (char *)malloc(sizeof(char) * 48);
			entry++;
		}
		record++;
	}
	return (dictionary);
}

