// Array TO Integer
int	ft_atoi(char *str)
{
	int		i;
	int		mult;
	int		nb;

	mult = 1;
	nb = 0;
	i = 0;
	while (*str == 32 || (*str >= 9 && *str <= 13))
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			mult *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = (nb * 10) + (str[i] - '0');
		i++;
	}
	nb *= mult;
	return (nb);
}

int	length(long nb)
{
	int	len;

	len = 0;
	while (nb)
	{
		nb /= 10;
		len++;
	}
	return (len);
}

void	convert_to_string(int nb, char *str, int len)
{
	int	is_neg;
	int	i;

	is_neg = nb < 0;
	if (is_neg)
	{
		*str = '-';
		nb = -nb;
	}
	str++;
	len--;
	i = len;
	while (nb)
	{
		str[i--] = '0' + nb % 10;
		nb /= 10;
	}
}

char	*ft_itoa(int nb)
{
	char	*str;
	int		len;

	len = calculate_length(nb);
	*str = malloc(len + 1);
	str[len] = '\0';
	convert_to_string(nb, str, len);
	return (str);
}

