#include "capy_head.h"
#include "variables.h"

int	is_the_same_str(char *s1, char *s2)
{
	unsigned int	c;

	c = 0;
	while (s1[c] == s2[c])
	{
		if (s1[c] == '\0')
		{
			return (1);
		}
		c++;
	}
	return (0);
}

