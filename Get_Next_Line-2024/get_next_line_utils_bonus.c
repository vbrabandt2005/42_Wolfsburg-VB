size_t	ft_strlen(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strchr(const char *str, int c)
{
	while (*str)
	{
		if (*str == (char)c)
			return ((char *)str);
		str++;
	}
	if (*str == (char)c)
		return ((char *)str);
	else
		return (NULL);
}

char	*ft_strdup(const char *str)
{
	char	*new;
	int		i;

	i = 0;
	new = (char *)malloc(sizeof(char) * ft_strlen(str) + 1);
	if (new == NULL)
		return (NULL);
	while (*str)
	{
		new[i] = *str++;
		i++;
	}
	new[i] = '\0';
	return (new);
}

char	*ft_strjoin(const char *str1, const char *str2)
{
	size_t			len1;
	size_t			len2;
	char			*slen;
	char			*start;

	len1 = ft_strlen(str1);
	len2 = ft_strlen(str2);
	slen = (char *)malloc(sizeof(char) * (len1 + len2 + 1));
	if (slen == NULL)
		return (NULL);
	start = slen;
	while (*str1)
		*slen++ = *str1++;
	while (*str2)
		*slen++ = *str2++;
	*slen = '\0';
	return (start);
}

char	*ft_substr(const char *str, unsigned int start, size_t len)
{
	unsigned int	i;
	char			*slen;

	i = 0;
	if (str == NULL)
		return (NULL);
	if (start > ft_strlen(str))
		len = 0;
	else if (len > (ft_strlen(str) - start))
		len = ft_strlen(str) - start;
	slen = malloc(sizeof(char) * (len + 1));
	if (slen == NULL)
		return (NULL);
	while (i < len && str[start])
	{
		slen[i] = str[start];
		i++;
		start++;
	}
	slen[i] = '\0';
	return (slen);
}

