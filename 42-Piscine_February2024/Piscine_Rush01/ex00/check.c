int	checkarg(char *str)
{
	int	i;
	int	num;

	i = 0;
	num = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '1' && str[i] <= '4')
		{
			num++;
		}
		i++;
	}
	if (num != 16)
	{
		return (0);
	}
	if (i != 31)
	{
		return (0);
	}
	return (1);
}
