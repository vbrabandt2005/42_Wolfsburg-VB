#include "get_next_line.h"

char	*ft_readfile(int fd, char *str, char *buffer)
{
	char	*tmp;
	int		buff_read;

	buff_read = 1;
	while (buff_read > 0)
	{
		buff_read = read(fd, buffer, BUFFER_SIZE);
		if (buff_read == -1)
		{
			free(str);
			return (NULL);
		}
		if (buff_read == 0)
			break ;
		buffer[buff_read] = '\0';
		if (str == NULL)
			str = ft_strdup("");
		tmp = str;
		str = ft_strjoin(tmp, buffer);
		free(tmp);
		tmp = NULL;
		if (ft_strchr(buffer, '\n'))
			break ;
	}
	return (str);
}

char	*ft_freeline(char *str)
{
	char	*line;
	int		i;

	i = 0;
	while (str[i] && str[i] != '\n')
		i++;
	if (str[i] == '\0' || str[1] == '\0')
		return (NULL);
	line = ft_substr(str, i + 1, ft_strlen(str) - i);
	if (*line == 0)
	{
		free(line);
		line = NULL;
	}
	str[i + 1] = '\0';
	return (line);
}

char	*get_next_line(int fd)
{
	char			*line;
	char			*buffer;
	static char		*str;	

	buffer = (char *)malloc(BUFFER_SIZE + 1);
	if (buffer == NULL)
		return (NULL);
	if (BUFFER_SIZE <= 0 || fd < 0 || read(fd, 0, 0) < 0)
	{
		free(str);
		free(buffer);
		str = NULL;
		buffer = NULL;
		return (NULL);
	}
	line = ft_readfile(fd, str, buffer);
	free(buffer);
	buffer = NULL;
	if (line == NULL)
		return (NULL);
	str = ft_freeline(line);
	return (line);
}

