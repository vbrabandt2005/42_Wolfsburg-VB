#include "ft_printf.h"

int	ft_print_char(char c)
{
	write(1, &c, 1);
	return (1);
}
// %c - print a single character

int	ft_print_str(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
	{
		return (ft_print_str("(null)"));
	}
	while (str[i])
	{
		ft_print_char(str[i]);
		i++;
	}
	return (i);
}
// %s - print a string

int	ft_print_percent(void)
{
	ft_print_char('%');
	return (1);
}
