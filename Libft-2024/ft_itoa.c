#include "libft.h"

static size_t	get_digits(int n)
{
	size_t	i;

	i = 0;
	if (n < 0)
		i++;
	if (n == 0)
		return (1);
	while (n != 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}

char	*ft_itoa(int n)
{
	char			*str_num;
	size_t			digits;
	unsigned int	num;

	digits = get_digits(n);
	str_num = (char *)malloc(sizeof(char) * (digits + 1));
	if (str_num == NULL)
		return (NULL);
	str_num[digits] = '\0';
	if (n < 0)
	{
		str_num[0] = '-';
		num = (unsigned int)(-n);
	}
	else
		num = (unsigned int)n;
	if (n == 0)
		str_num[0] = '0';
	while (num != 0)
	{
		str_num[--digits] = (num % 10) + '0';
		num = num / 10;
	}
	return (str_num);
}

