#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	s_len;
	size_t	sub_len;
	char	*substr;
	char	*temp_s;

	temp_s = strdup(s);
	if (temp_s == NULL)
		return (NULL);
	s_len = ft_strlen(temp_s);
	if (start > s_len)
		len = 0;
	if (len > s_len - start)
		sub_len = s_len - start;
	else
		sub_len = len;
	if (sub_len == 0)
	{
		substr = (char *)malloc(sizeof(char));
		if (substr == NULL)
			return (NULL);
		*substr = '\0';
	}
	else
	{
		substr = (char *)malloc((sub_len + 1) * sizeof(char));
		if (substr == NULL)
			return (NULL);
		ft_strlcpy(substr, s + start, sub_len + 1);
	}
	return (substr);
}

