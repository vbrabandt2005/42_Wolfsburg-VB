#include "libft.h"

void *ft_memmove(void *dest, const void *src, size_t len)
{
    unsigned char *d = (unsigned char *)dest;
    const unsigned char *s = (const unsigned char *)src;

    if (d < s)
    {
        while (len--)
            *d++ = *s++;
    }
    else
    {
        unsigned char *lasts = (unsigned char *)s + (len - 1);
        unsigned char *lastd = d + (len - 1);
        while (len--)
            *lastd-- = *lasts--;
    }

    return dest;
}

