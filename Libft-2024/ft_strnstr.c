#include "libft.h"

char *ft_strnstr(const char *s, const char *find, size_t slen) {
	char c, sc;
	size_t len;
	size_t i;

	if ((c = *find++) != '\0') {
		len = ft_strlen(find);
		while (slen-- > 0) {
			sc = *s++;
			if (sc == '\0')
				return NULL;
			if (sc == c)
			{
				i = 0;
				while (i < len && slen > 0 && s[i] == find[i])
				{
					i++;
					slen--;
				}
				if (i == len) 
				{
					return (char *)s - len;
				}
			}
		}
	}
	return NULL;
}

